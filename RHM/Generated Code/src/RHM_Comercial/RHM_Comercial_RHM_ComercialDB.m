+ (void)waitForKeyGenerator
{
    BOOL shouldWait = YES;
    long sleepTime = 1000;
    long timeout = 20*1000;
    while (shouldWait && (sleepTime < timeout))
    {
        shouldWait = NO;
        {
        	SUPObjectList *kg = [RHM_Comercial_KeyGenerator findAll];
        	if([kg size] == 0)
        		shouldWait = YES;
        }
        if (shouldWait)
        {
            [SUPThreadUtil sleep:sleepTime];
        }
        if (sleepTime <= timeout)
        {
            timeout = timeout - sleepTime;
        }
    }        
    if (shouldWait) {
		MBOLogPersistenceException(SUPCLIENTRT_TEMPLATE_ILLEGALKEYGENERATORSTATUS);
    }
}

