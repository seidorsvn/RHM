- (void)onSynchronizeResponse:(SUPJsonArray*)a:(int32_t)syncStatus
{
	if((a == nil) || ([a size] == 0)) return;
	SUPJsonObject *o = [a item:0];
    NSString *syncGroups = nil;
    NSString *context = nil;
    if(o)
    {
        if([o containsKey:@"context"])
            context = [o getNullableString:@"context"];
        if([o containsKey:@"group"])
            syncGroups = [o getNullableString:@"group"];
        else if([o containsKey:@"groups"])
            syncGroups = [o getNullableString:@"groups"];
    }
    if(!syncGroups) syncGroups = @"";
    if(!context) context = @"";
    SUPSynchronizationContext *syncContext = [SUPSynchronizationContext getInstance];
    syncContext.userContext = context;
    SUPObjectList *sgs = [SUPObjectList getInstance];
    for(SUPString sgname in [SUPStringList split:syncGroups:@","])
    {
    	RHM_Comercial_SynchronizationGroup *sg = [RHM_Comercial_RHM_ComercialDB getSyncGroupForName:sgname];
    	if(sg)
        	[sgs add:sg];
    }
    syncContext.status = syncStatus;
    if([self callbackHandler])
    	[[self callbackHandler] onSynchronize:sgs withContext:syncContext];
}

