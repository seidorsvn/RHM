+ (void)beginSynchronize:(NSString*)synchronizationGroups:(NSString*)context
{
	SUPObjectList *sgs = nil;
	if(synchronizationGroups == nil)
		sgs = [self getAllSynchronizationGroups];
	else
	{
		SUPStringList *syncGroupList = [SUPStringList split:synchronizationGroups:@","];
		if([syncGroupList size] == 0)
			sgs = [self getAllSynchronizationGroups];
		else
		{
			sgs = [SUPObjectList getInstance];
			for(SUPString sgname in syncGroupList)
			{
				RHM_Comercial_SynchronizationGroup *sg = [self getSyncGroupForName:sgname];
				if(sg) [sgs add:sg];
			}
		}	
	}
	[self beginSynchronize:sgs withContext:context];
}

+ (void)beginSynchronize:(SUPObjectList*)synchronizationGroups withContext:(NSString*)context;
{
	SUPSynchronizationContext *syncContext = [SUPSynchronizationContext getInstance];
	syncContext.userContext = context;

	SUPObjectList *sgs = nil;
	if(synchronizationGroups == nil)
		sgs = [self getAllSynchronizationGroups];
	else
		sgs = synchronizationGroups;

    if(![self packageHasSubscription])
    {
        if([[self instance] callbackHandler])
        {
            syncContext.status = SUPSynchronizationStatusERROR;
            syncContext.exception = [[[SUPSynchronizeException alloc] initWithName:@"SUPSynchronizeException" reason:SUPCLIENTRT_PACKAGENOTSUBSCRIBED userInfo:nil] autorelease];
            [[[self instance] callbackHandler] onSynchronize:sgs withContext:syncContext];
        }
        return;
    }
	
	NSMutableString *syncGroupString = [[NSMutableString alloc] init];
	BOOL first = YES;
	for(RHM_Comercial_SynchronizationGroup *sg in sgs)
	{
		if(!first)
			[syncGroupString appendString:@","];
		[syncGroupString appendString:[sg name]];
		first = NO;
	}
	syncContext.status = [SUPSynchronizationStatus STARTING];
	if([[self instance] callbackHandler])
	{
		int32_t action = [[[self instance] callbackHandler] onSynchronize:sgs withContext:syncContext];
		if(action == [SUPSynchronizationAction CANCEL])
		{
			[syncGroupString release];
			return;
		}
	}
	SUPJsonObject *content = [SUPJsonObject getInstance];
	[content setString:@"groups":[syncGroupString description]];
	[content setNullableString:@"context":context];
	[[self instance] beginSynchronize:[RHM_Comercial_LocalKeyGenerator generateId] withContent:content];
    [syncGroupString release];
}

