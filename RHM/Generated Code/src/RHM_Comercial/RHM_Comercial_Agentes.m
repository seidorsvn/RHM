- (SUPAbstractEntity*)downloadState
{
    if (!self->_downloadStateValid)
    {
        if(self.isNew) 
        {
            self->_downloadState = nil;
        }
        else if(self.pending)
        {
            RHM_Comercial_Agentes *mbo = [RHM_Comercial_Agentes find:[self id]];
            if(mbo)
